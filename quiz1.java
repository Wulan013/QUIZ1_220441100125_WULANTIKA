    private void menuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuActionPerformed
        // TODO add your handling code here:
        switch (menu.getSelectedIndex()){
            case 1:
            {
                pesanan.setText(String.valueOf("Cappucino"));
                eharga.setText(String.valueOf("10000"));
            }
            break;
            case 2:
            {
                pesanan.setText(String.valueOf("Latte"));
                eharga.setText(String.valueOf("13000"));
                
            }
            break;
            case 3:
            {
                pesanan.setText(String.valueOf("Americano"));
                eharga.setText(String.valueOf("14000"));
            }
            break;
            case 4:
            {
                pesanan.setText(String.valueOf("Expreso"));
                eharga.setText(String.valueOf("12000"));
            }
            break;
     
    }
    
    }//GEN-LAST:event_menuActionPerformed

    private void bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bayarActionPerformed
        // TODO add your handling code here:
        double harga, jumlah, total;
            harga = Double.parseDouble(eharga.getText());
            jumlah = Double.parseDouble(ejumlah.getText());
            total = harga * jumlah;
            etotal.setText(String.valueOf(total));
    }//GEN-LAST:event_bayarActionPerformed

    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        // TODO add your handling code here:
        double harga, bayar, hasil;
            harga = Double.parseDouble(etotal.getText());
            bayar = Double.parseDouble(uang.getText());
            hasil = bayar - harga;
            kembalian.setText(String.valueOf(hasil));
    }//GEN-LAST:event_pesanActionPerformed

